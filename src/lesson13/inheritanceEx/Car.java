package lesson13.inheritanceEx;

public class Car {
	
int maxSpeed;
String color;
String model;
int weigth;


public Car() {
	super();
}

public Car(int maxSpeed, String color, String model, int weigth) {
	super();
	this.maxSpeed = maxSpeed;
	this.color = color;
	this.model = model;
	this.weigth = weigth;
}


}
